def split_title_year(film_key: str) -> Tuple[str, float]:
    """Split 'Title (YYYY)' into (Title, YYYY)."""
    m = re.match(r"^(.*)\s+\((\d{4})\)\s*$", str(film_key))
    if not m:
        return str(film_key), np.nan
    return m.group(1), float(m.group(2))


def prepare_user(df: pd.DataFrame) -> pd.DataFrame:
    """Add title, year, title_norm columns to user dataframe."""
    out = df.copy()
    ty = out["film"].apply(split_title_year)
    out["title"] = ty.apply(lambda x: x[0])
    out["year"] = ty.apply(lambda x: x[1])
    out["title_norm"] = out["title"].astype(str).apply(normalize_title_full)
    return out


def attach_imdb(user_df: pd.DataFrame) -> pd.DataFrame:
    """Join user films to IMDb by (title_norm, year)."""
    u = prepare_user(user_df)
    lookup = build_imdb_lookup()
    u = u.join(lookup, on=["title_norm", "year"], rsuffix="_imdb")
    return u


