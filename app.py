def jaccard_overlap(df1: pd.DataFrame, df2: pd.DataFrame) -> float:
    s1, s2 = set(df1["film"]), set(df2["film"])
    if len(s1 | s2) == 0:
        return 0.0
    return len(s1 & s2) / len(s1 | s2)


def pearson_corr_common(df1: pd.DataFrame, df2: pd.DataFrame, min_common: int = 5):
    merged = df1.merge(df2, on="film", suffixes=("_u1", "_u2"))
    # Use rating_model for compatibility math
    merged = merged.dropna(subset=["rating_model_u1", "rating_model_u2"])
    if len(merged) < min_common:
        return 0.0, merged
    corr = np.corrcoef(merged["rating_model_u1"], merged["rating_model_u2"])[0, 1]
    if np.isnan(corr):
        corr = 0.0
    return float(max(corr, 0.0)), merged


def mad_norm(merged: pd.DataFrame) -> float:
    """Mean absolute rating difference normalized by 5 (uses rating_model)."""
    if len(merged) == 0:
        return 1.0
    diff = np.mean(np.abs(merged["rating_model_u1"] - merged["rating_model_u2"]))
    return float(diff / 5.0)


def compatibility_score(df1: pd.DataFrame, df2: pd.DataFrame):
    overlap = jaccard_overlap(df1, df2)
    corr, merged = pearson_corr_common(df1, df2)
    mad = mad_norm(merged)

    score = 0.35 * overlap + 0.45 * corr + 0.20 * (1 - mad)
    return float(score), {
        "overlap": overlap,
        "corr": corr,
        "mad_norm": mad,
        "common_films": len(merged),
    }


def match_label(score_0_1: float) -> str:
    s = float(np.clip(score_0_1, 0.0, 1.0))
    if s < 0.33:
        return "Low match"
    elif s < 0.66:
        return "Medium match"
    return "High match"


def badge_style(label: str) -> str:
    if label.startswith("Low"):
        return "background: rgba(239,68,68,0.15); border: 1px solid rgba(239,68,68,0.35); color:#fecaca;"
    if label.startswith("Medium"):
        return "background: rgba(234,179,8,0.15); border: 1px solid rgba(234,179,8,0.35); color:#fde68a;"
    return "background: rgba(34,197,94,0.15); border: 1px solid rgba(34,197,94,0.35); color:#bbf7d0;"


