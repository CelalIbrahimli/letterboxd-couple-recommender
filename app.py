def normalize_title_full(s: str) -> str:
    """Normalize titles for matching: lowercase, remove punctuation, normalize spaces."""
    s = str(s).lower().strip()
    s = re.sub(r"[^a-z0-9\s]", "", s)
    s = re.sub(r"\s+", " ", s).strip()
    return s


@lru_cache(maxsize=1)
def load_imdb_index() -> pd.DataFrame:
    """Load IMDb parquet once per container lifecycle."""
    imdb = pd.read_parquet(IMDB_PATH)

    # Ensure numeric
    imdb["startYear"] = pd.to_numeric(imdb.get("startYear"), errors="coerce")
    imdb["averageRating"] = pd.to_numeric(imdb.get("averageRating"), errors="coerce")
    imdb["numVotes"] = pd.to_numeric(imdb.get("numVotes"), errors="coerce")
    if "runtimeMinutes" in imdb.columns:
        imdb["runtimeMinutes"] = pd.to_numeric(imdb.get("runtimeMinutes"), errors="coerce")

    # Add normalized title if missing
    if "primary_norm" not in imdb.columns:
        if "primaryTitle" not in imdb.columns:
            raise ValueError("IMDb parquet must contain 'primaryTitle' column.")
        imdb["primary_norm"] = imdb["primaryTitle"].astype(str).apply(normalize_title_full)

    return imdb


@lru_cache(maxsize=1)
def build_imdb_lookup() -> pd.DataFrame:
    """
    Build a unique lookup:
      (primary_norm, startYear) -> best IMDb row (highest numVotes when duplicates exist)
    Stored as a multi-indexed DataFrame for fast join.
    """
    imdb = load_imdb_index()
    imdb2 = imdb.dropna(subset=["startYear"]).copy()
    imdb2 = imdb2.sort_values("numVotes", ascending=False)
    lookup = (
        imdb2.drop_duplicates(subset=["primary_norm", "startYear"])
        .set_index(["primary_norm", "startYear"])
    )
    return lookup


