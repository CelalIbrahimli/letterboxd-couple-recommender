def compute_all(zip1_file: str, zip2_file: str):
    zip1 = Path(zip1_file)
    zip2 = Path(zip2_file)

    user1 = build_user_df_from_zip(zip1)
    user2 = build_user_df_from_zip(zip2)

    user1m = attach_imdb(user1)
    user2m = attach_imdb(user2)

    match1 = float(user1m["tconst"].notna().mean()) if "tconst" in user1m.columns else 0.0
    match2 = float(user2m["tconst"].notna().mean()) if "tconst" in user2m.columns else 0.0

    comp, details = compatibility_score(user1, user2)

    # ---------- SHARED TOP 20 (both watched + both rated, sorted by avg rating) ----------
    shared = user1.merge(user2, on="film", suffixes=("_u1", "_u2"))
    shared["avg_real_rating"] = (shared["rating_u1"] + shared["rating_u2"]) / 2

    # Only keep where both have REAL ratings (not NaN)
    shared_top = shared.dropna(subset=["rating_u1", "rating_u2"]).copy()

    # Attach IMDb rating
    shared_p = prepare_user(shared_top[["film"]].copy())
    lookup = build_imdb_lookup()
    shared_meta = shared_p.join(lookup, on=["title_norm", "year"])
    shared_top["imdb_rating"] = shared_meta.get("averageRating")

    shared_top = shared_top.sort_values("avg_real_rating", ascending=False).head(20).reset_index(drop=True)

    shared_top_out = shared_top[["film", "imdb_rating", "rating_u1", "rating_u2"]].copy()
    shared_top_out = shared_top_out.rename(
        columns={
            "film": "FILM",
            "imdb_rating": "IMDB",
            "rating_u1": "USER1 RATING",
            "rating_u2": "USER2 RATING",
        }
    )

    # ---------- ONE WATCHED, OTHER DIDN'T (outer merge on union) ----------
    u1 = user1[["film", "rating"]].rename(columns={"rating": "rating_u1"})
    u2 = user2[["film", "rating"]].rename(columns={"rating": "rating_u2"})
    union = u1.merge(u2, on="film", how="outer")

    # Attach IMDb rating
    union_p = prepare_user(union[["film"]].copy())
    union_meta = union_p.join(lookup, on=["title_norm", "year"])
    union["imdb_rating"] = union_meta.get("averageRating")

    # Mark who watched
    union["USER1 WATCHED"] = union["rating_u1"].notna()
    union["USER2 WATCHED"] = union["rating_u2"].notna()

    # Show only cases where exactly one watched (xor)
    one_watched = union[(union["USER1 WATCHED"] ^ union["USER2 WATCHED"])].copy()

    # Convert ratings to display "-" if missing
    def disp(x):
        return "-" if pd.isna(x) else float(x)

    one_watched_out = one_watched[["film", "imdb_rating", "rating_u1", "rating_u2", "USER1 WATCHED", "USER2 WATCHED"]].copy()
    one_watched_out["rating_u1"] = one_watched_out["rating_u1"].apply(disp)
    one_watched_out["rating_u2"] = one_watched_out["rating_u2"].apply(disp)

    one_watched_out = one_watched_out.rename(columns={
        "film": "FILM",
        "imdb_rating": "IMDB",
        "rating_u1": "USER1 RATING",
        "rating_u2": "USER2 RATING",
    })

    # Sort: show highest IMDb first (nice UX)
    one_watched_out["IMDB_sort"] = pd.to_numeric(one_watched_out["IMDB"], errors="coerce")
    one_watched_out = one_watched_out.sort_values("IMDB_sort", ascending=False).drop(columns=["IMDB_sort"]).reset_index(drop=True)

    # ---------- Genre table ----------
    g1 = genre_profile_from_meta(user1m)
    g2 = genre_profile_from_meta(user2m)
    all_g = sorted(set(g1.index) | set(g2.index))
    g1n = g1.reindex(all_g).fillna(0)
    g2n = g2.reindex(all_g).fillna(0)
    g1n = g1n / (g1n.sum() or 1)
    g2n = g2n / (g2n.sum() or 1)
    joint = (g1n + g2n) / 2
    joint = joint / (joint.sum() or 1)

    g_df = (
        pd.DataFrame(
            {
                "user1": g1n.sort_values(ascending=False).head(12),
                "user2": g2n.sort_values(ascending=False).head(12),
                "joint": joint.sort_values(ascending=False).head(12),
            }
        )
        .fillna(0)
        .reset_index()
        .rename(columns={"index": "genre"})
    )

    state = {
        "user1": user1,
        "user2": user2,
        "user1m": user1m,
        "user2m": user2m,
        "g1n": g1n,
        "g2n": g2n,
    }

    # ---------- Summary UI ----------
    comp01 = float(np.clip(comp, 0.0, 1.0))
    pct = comp01 * 100
    label = match_label(comp01)
    badge = badge_style(label)

    big = f"""
<div style="text-align:center; padding: 10px 8px; margin-top: 6px;">
  <div style="font-size: 28px; font-weight: 900; color: #22c55e;">
    YOUR SHARED TASTE: {pct:.1f}%
  </div>

  <div style="max-width: 520px; margin: 10px auto 6px auto;">
    <div style="height: 10px; width: 100%; background: rgba(255,255,255,0.10); border-radius: 999px; overflow: hidden;">
      <div style="height: 100%; width: {pct:.1f}%; background: #22c55e;"></div>
    </div>
  </div>

  <div style="display:flex; justify-content:center; gap:10px; align-items:center; margin-top: 6px;">
    <span style="padding: 4px 10px; border-radius: 999px; font-size: 12px; {badge}">
      {label}
    </span>
    <span style="opacity:0.80; font-size: 12px;">
      (based on overlap + rating agreement)
    </span>
  </div>
</div>
"""

    stats = f"""
<div style="margin-top: 8px; padding: 10px 12px; border: 1px solid rgba(255,255,255,0.10); border-radius: 14px;">
  <div style="font-size: 13px; line-height: 1.65; opacity:0.92;">
    <b>Common films:</b> {details['common_films']}<br>
    <b>Overlap (Jaccard):</b> {details['overlap']:.3f}<br>
    <b>Rating agreement (Pearson, clamped):</b> {details['corr']:.3f}<br>
    <b>Mean abs diff (norm):</b> {details['mad_norm']:.3f}<br>
    <b>IMDb match rate:</b> User1 {match1*100:.1f}% | User2 {match2*100:.1f}%
  </div>
</div>
"""
    summary_md = big + stats

    # Download CSV (shared top 20)
    blend_csv_path = Path("/tmp/blend_shared_top20.csv")
    shared_top_out.to_csv(blend_csv_path, index=False)

    return summary_md, shared_top_out, one_watched_out, g_df, str(blend_csv_path), state


def recommend(state: Dict, mood: str, top_k: int):
    imdb = load_imdb_index()

    user1 = state["user1"]
    user2 = state["user2"]
    g1n = state["g1n"]
    g2n = state["g2n"]

    mood_val = None if mood == "none" else mood
    g1m = apply_mood(g1n, mood_val)
    g2m = apply_mood(g2n, mood_val)

    # Unseen by either user (watched set)
    seen = set(user1["film"]) | set(user2["film"])

    cand = imdb.dropna(subset=["startYear", "primaryTitle", "averageRating", "numVotes", "genres"]).copy()
    cand["film"] = cand["primaryTitle"].astype(str) + " (" + cand["startYear"].astype(int).astype(str) + ")"
    cand = cand[~cand["film"].isin(seen)].copy()

    cand = cand[(cand["numVotes"] >= 5000) & (cand["averageRating"] >= 6.5) & (cand["startYear"] >= 1960)].copy()

    cand["score_u1"] = cand.apply(lambda r: score_for_user(r["genres"], r["averageRating"], r["numVotes"], g1m), axis=1)
    cand["score_u2"] = cand.apply(lambda r: score_for_user(r["genres"], r["averageRating"], r["numVotes"], g2m), axis=1)
    cand["couple_score"] = cand.apply(lambda r: harmonic(r["score_u1"], r["score_u2"]), axis=1)
    cand["explore_bonus"] = cand["numVotes"].apply(exploration_bonus)
    cand["final_rel"] = cand["couple_score"] + cand["explore_bonus"]

    top = mmr_rerank(cand, k=int(top_k), lambda_=0.78, rel_col="final_rel")

    if "runtimeMinutes" not in top.columns:
        top["runtimeMinutes"] = np.nan

    # Convert scores to % (0–100) for UI (but NO overall column now)
    def to_pct(x):
        try:
            return float(np.clip(float(x), 0.0, 1.0)) * 100.0
        except Exception:
            return np.nan

    out = top[[
        "film",
        "genres",
        "averageRating",
        "runtimeMinutes",
        "score_u1",
        "score_u2",
        "couple_score",
    ]].copy()

    out["USER1 SCORE (%)"] = out["score_u1"].apply(to_pct)
    out["USER2 SCORE (%)"] = out["score_u2"].apply(to_pct)
    out["COUPLE SCORE (%)"] = out["couple_score"].apply(to_pct)

    out = out.rename(columns={
        "film": "FILM",
        "genres": "GENRE",
        "averageRating": "IMDB",
        "runtimeMinutes": "RUNTIME",
    })

    out_final = out[[
        "FILM",
        "GENRE",
        "IMDB",
        "RUNTIME",
        "USER1 SCORE (%)",
        "USER2 SCORE (%)",
        "COUPLE SCORE (%)",
    ]].copy()

    out_final["IMDB"] = pd.to_numeric(out_final["IMDB"], errors="coerce").round(2)
    for c in ["USER1 SCORE (%)", "USER2 SCORE (%)", "COUPLE SCORE (%)"]:
        out_final[c] = pd.to_numeric(out_final[c], errors="coerce").round(1)
    out_final["RUNTIME"] = pd.to_numeric(out_final["RUNTIME"], errors="coerce")

    rec_csv_path = Path("/tmp/recommendations.csv")
    out_final.to_csv(rec_csv_path, index=False)

    years = out_final["FILM"].str.extract(r"\((\d{4})\)")[0].astype(float)
    year_spread = float(years.max() - years.min()) if years.notna().any() else 0.0

    md = (
        f"### 🎯 Recommendations\n"
        f"- **Mood:** `{mood}`\n"
        f"- **Unique genres:** `{out_final['GENRE'].nunique()}`\n"
        f"- **Year spread:** `{year_spread:.0f}` years\n"
    )

    return md, out_final, str(rec_csv_path)


