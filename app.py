def read_csv_from_zip(zip_path: Path, filename: str) -> Optional[pd.DataFrame]:
    """Read a CSV by suffix match from Letterboxd export ZIP."""
    with zipfile.ZipFile(zip_path, "r") as z:
        members = [m for m in z.namelist() if m.lower().endswith(filename.lower())]
        if not members:
            return None
        with z.open(members[0]) as f:
            return pd.read_csv(f)


def build_user_df_from_zip(zip_path: Path, neutral_rating: float = 3.0) -> pd.DataFrame:
    """
    Returns a dataframe with:
      film, rating (NaN if unrated), rating_model (rating filled with neutral), watched(True), rated(True/False)

    - ratings.csv supplies rated films
    - watched.csv supplies watched films (may be unrated)
    """
    ratings = read_csv_from_zip(zip_path, "ratings.csv")
    watched = read_csv_from_zip(zip_path, "watched.csv")
    if ratings is None or watched is None:
        raise ValueError("ZIP must contain ratings.csv and watched.csv")

    for col in ["Name", "Year"]:
        if col not in watched.columns:
            raise ValueError(f"watched.csv missing column: {col}")
    for col in ["Name", "Year", "Rating"]:
        if col not in ratings.columns:
            raise ValueError(f"ratings.csv missing column: {col}")

    ratings = ratings.copy()
    watched = watched.copy()

    ratings["film"] = ratings["Name"].astype(str) + " (" + ratings["Year"].astype(int).astype(str) + ")"
    watched["film"] = watched["Name"].astype(str) + " (" + watched["Year"].astype(int).astype(str) + ")"

    # Rated
    ratings_clean = ratings[["film", "Rating"]].rename(columns={"Rating": "rating"}).dropna()
    ratings_clean["rating"] = pd.to_numeric(ratings_clean["rating"], errors="coerce")
    ratings_clean = ratings_clean.dropna(subset=["rating"])
    ratings_clean["watched"] = True
    ratings_clean["rated"] = True

    # Watched but unrated -> rating stays NaN (DISPLAY), but rating_model will fill neutral (SCORING)
    watched_only = watched[~watched["film"].isin(ratings_clean["film"])].copy()
    watched_only["rating"] = np.nan
    watched_only = watched_only[["film", "rating"]]
    watched_only["watched"] = True
    watched_only["rated"] = False

    user_df = pd.concat([ratings_clean, watched_only], ignore_index=True).reset_index(drop=True)
    user_df["rating_model"] = user_df["rating"].fillna(float(neutral_rating))
    return user_df


