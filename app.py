MOOD_PRESETS = {
    "romantic": {"Romance": 1.4, "Drama": 1.2},
    "fun": {"Comedy": 1.5, "Adventure": 1.2},
    "dark": {"Thriller": 1.4, "Horror": 1.3},
    "epic": {"Action": 1.4, "Fantasy": 1.3},
}


def genre_profile_from_meta(user_meta: pd.DataFrame, like_threshold: float = 4.0) -> pd.Series:
    # Use rating_model for consistent scoring even if unrated
    liked = user_meta[user_meta["rating_model"] >= like_threshold].copy()
    liked = liked.dropna(subset=["genres"])
    g = liked["genres"].astype(str).str.split(",").explode()
    return g.value_counts(normalize=True)


def apply_mood(genre_dist: pd.Series, mood: Optional[str]) -> pd.Series:
    g = genre_dist.copy()
    if mood and mood in MOOD_PRESETS:
        for k, v in MOOD_PRESETS[mood].items():
            if k in g.index:
                g.loc[k] *= v
    s = g.sum()
    return g / (s if s else 1.0)


def harmonic(a: float, b: float) -> float:
    return 2 * a * b / (a + b + 1e-9)


def exploration_bonus(numVotes: float) -> float:
    if pd.isna(numVotes):
        return 0.0
    v = float(numVotes)
    if v < 20000:
        return 0.05
    elif v < 100000:
        return 0.02
    return 0.0


def score_for_user(
    genres_str: str,
    imdb_rating: float,
    numVotes: float,
    user_genre: pd.Series,
    min_votes: int = 5000,
) -> float:
    gs = str(genres_str).split(",") if pd.notna(genres_str) else []
    genre_score = float(user_genre.reindex(gs).fillna(0).sum())

    if pd.isna(imdb_rating):
        quality = 0.0
    else:
        reliability = 1.0 if (pd.notna(numVotes) and float(numVotes) >= min_votes) else 0.6
        quality = (float(imdb_rating) / 10.0) * reliability

    return 0.75 * genre_score + 0.25 * quality


def genre_set(genres_str: str) -> set:
    return set(str(genres_str).split(",")) if pd.notna(genres_str) else set()


def jaccard(a: set, b: set) -> float:
    if not a and not b:
        return 0.0
    return len(a & b) / len(a | b)


def year_diversity_bonus(selected_years, cand_year) -> float:
    if not selected_years:
        return 0.0
    years = [y for y in selected_years if pd.notna(y)]
    if not years or pd.isna(cand_year):
        return 0.0
    avg = float(np.mean(years))
    return abs(float(cand_year) - avg) / 100.0


def mmr_rerank(df: pd.DataFrame, k: int = 10, lambda_: float = 0.78, rel_col: str = "final_rel") -> pd.DataFrame:
    df = df.copy()
    df["gset"] = df["genres"].apply(genre_set)

    selected_rows = []
    selected_years = []
    candidates = df.sort_values(rel_col, ascending=False).to_dict("records")

    while len(selected_rows) < k and candidates:
        best_idx, best_score = None, -1e9

        for i, cand in enumerate(candidates):
            rel = float(cand.get(rel_col, 0.0))
            cand_g = cand["gset"]
            cand_y = cand.get("startYear", np.nan)

            if not selected_rows:
                div_pen = 0.0
            else:
                sims = [jaccard(cand_g, s["gset"]) for s in selected_rows]
                div_pen = max(sims)

            y_bonus = year_diversity_bonus(selected_years, cand_y)

            mmr = lambda_ * rel - (1 - lambda_) * div_pen + 0.05 * y_bonus
            if mmr > best_score:
                best_score = mmr
                best_idx = i

        chosen = candidates.pop(best_idx)
        selected_rows.append(chosen)
        selected_years.append(chosen.get("startYear", np.nan))

    out = pd.DataFrame(selected_rows).drop(columns=["gset"])
    return out


